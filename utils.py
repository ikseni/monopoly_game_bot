import random


def send_msg(vk, peer_id, text, keyboard=None, attachment=None):
    params = {
        'peer_id': peer_id,
        'message': text,
        'random_id': random.randint(1, 2**31 - 1)
    }
    if keyboard:
        params['keyboard'] = keyboard
    if attachment:
        params['attachment'] = attachment
    vk.messages.send(**params)


